package primeirob.exemplos;

public class exemplosPrimeiraAula {
    public static void main(String[] args) {
        byte parcelas = 36;
        System.out.println("byte: " + parcelas);
       
        short idade = 100;
        System.out.println("short: " + idade);

        int cep = 12345678;
        System.out.println("int: " + cep);
        
        long numeroCelular = 12345678910l;
        System.out.println("long: " + numeroCelular);
        
        float mora = 0.33f;
        System.out.println("float: " + mora);

        double juros = 50.1;
        System.out.println("double: " + juros);

        char letraA = 65;
        System.out.println("char: " + letraA);
    }
}
